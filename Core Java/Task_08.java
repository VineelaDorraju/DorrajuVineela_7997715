//Task 8: Operator Precedence

class Task_08 {
    public static void main(String[] args) {
        int result = 10 + 5 * 2;
        System.out.println("Result of 10 + 5 * 2 = " + result);
        System.out.println("Explanation: Multiplication (*) has higher precedence than addition (+). Therefore, 5 * 2 is evaluated first to get 10, and then 10 + 10 results in 20.");
    }
}
