//Task 7: Type Casting Example

class Task_07 {
    public static void main(String[] args) {
        double originalDouble = 45.67;
        int castedInt = (int) originalDouble;
        System.out.println("Original Double: " + originalDouble);
        System.out.println("Casted to Integer: " + castedInt);

        int originalInt = 100;
        double castedDouble = (double) originalInt;
        System.out.println("Original Integer: " + originalInt);
        System.out.println("Casted to Double: " + castedDouble);
    }
}
